                const sidenav = document.getElementById("mySidenav");
                if (sidenav.style.width === "100%") {
                    sidenav.style.width = "0";
                } else {
                    sidenav.style.width = "100%";
                }
            }

            function closeNav() {
                document.getElementById("mySidenav").style.width = "0";
            }
